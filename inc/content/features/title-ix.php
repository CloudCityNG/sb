            <div class="content milestones">
                <h3>Key Milestones</h3>
                <p><span>September 2012</span>SBU awarded $270,000 from the Department of Justice: Office on Violence Against Women for campus programs focused on the reduction of domestic violence, dating violence, sexual assault and stalking.</p>

                <p><span>June 2013</span>Title IX Committee forms while key administrators complete Association of Title IX Administrators (ATIXA) investigation training.</p>

                <p><span>December 2013</span>Phase one of “Hear It, See It, Report It” campaign launches to improve SBU’s Title IX policies and procedures by calling for input from individuals who have reported or experienced sexual misconduct on campus.</p>

                <p><span>February 2014</span>Phase two of “Hear It, See It, Report It” campaign distributes poster listing Title IX resources and sexual misconduct reporting procedures.</p>

                <p><span>March 2014</span>University Student Conduct Code is updated reflecting finalized complainant procedure that defines sexual harassment, sexual assault, and sexual violence.</p>

                <p><span>April 2014</span>SBU’s fifth annual campus climate survey, Polaris, is sent to all students gathering input that may relate to Title IX issues.</p>

                <p><span>April 2014</span>SBU chosen by the Department of Justice to serve as one of the locations for its national campus tour, coinciding with the White House Task Force to Protect Students from Sexual Assault’s release of the report titled “Not Alone.”</p>

                <p><span>August 2014</span>Over 700 faculty, staff and students receive in-person Title IX training, including all Residence Hall Directors and Resident Assistants from on-campus housing.</p>

                <p><span>September 2014</span>Mandatory online Title IX/Clery Act training for all students launches.</p>

                <p><span>October 2014</span>Stony Brook Council approves University Student Conduct Code revisions after the SUNY Board of Trustees votes to establish system wide uniform policies and practices on sexual misconduct.</p>

                <p><span>December 2014</span>Twenty-five Deputy Title IX Coordinators are identified across the college to improve access to qualified points of contact when reporting Title IX complaints.</p>

                <p><span>January 2015</span>All Deputy Title IX Coordinators receive full training by ATIXA Executive Director Brett Sokolow.</p>

                <p><span>January 2015</span>Key administrators and faculty receive four-day Green Dot Training for bystander intervention tactics.</p>
