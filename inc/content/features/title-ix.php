            <h3>Stony Brook Makes Title IX Strides with Initiatives, Training and New Website</h3>
            <div class="content col-2 col-2-left">
                <p>When it comes to Title IX, Stony Brook University works hard and plays fair.</p>
                <p>Our staff is dedicated to protect and assist individuals on campus with Title IX issues while developing initiatives and resources to keep our campus safe from sexual discriminataaion and misconduct.</p>
                <h4>What exactly is Title IX?</h4>
                <p>Title IX is a comprehensive federal law passed in 1972 that prohibits sexual discrimination in every educational program or activity that is federally funded.</p>
                <p>At Stony Brook, the law protects all students, faculty and staff from sexual discrimination including all forms of sexual misconduct, harassment and violence.</p>
