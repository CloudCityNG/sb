                <h4>Stony Brook’s Coordinated Staff Effort</h4>
                <p>With direction from Title IX Coordinator Marjolie Leonard in the Office of Diversity and Affirmative Action, key staff from Employee and Labor Relations, the Office of University Community Standards, and within the Student Affairs Division including the Center for Prevention and Outreach are driving a collaborative and ongoing effort to better campus safety and eliminate sexual discrimination at Stony Brook University.</p>
