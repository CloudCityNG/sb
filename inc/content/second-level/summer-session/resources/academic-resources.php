                <p><h5>Graded/Pass/No Credit Option (GPNC)</h5>
                <p>GPNC. Some institutions have restrictions on the grading of GPNC courses. Stony Brook students should refer to the policy on GPNC in the <a href="http://sb.cc.stonybrook.edu/bulletin/current/" target="_blank">Undergraduate Bulletin</a>.</p>
                <h5>Incompletes</h5>
                <p>If an instructor agrees to assign an Incomplete in a course, the instructor will determine the date by which you must complete the work. However, the date may not be later than November 1, otherwise the grade of I will automatically be changed to I/F and calculated as a failing grade.</p>
                <h5>No Record</h5>
                <p>Grades of No Record (NR) which have not been replaced by a final grade or by a W by the end of the ninth week of the fall semester (for spring and summer NR grades) will be converted to one of the following grades:</p>
                <ul>
                    <li>N/F for letter-graded courses</li>
                    <li>N/U for courses graded A-C/U or S/U</li>
                    <li>N/C for courses taken under the Graded Pass No Credit option</li>
                </ul>
                <p>The grade of N/F will be treated as a failure for the purpose of academic standing and will be averaged as an F when the student's grade point average is computed.</p>
                <h5>Grade Reports</h5>
                <p>You may access your grades on the <a href="http://it.stonybrook.edu/services/solar" target="_blank">SOLAR System</a>. If you need to have a transcript mailed to your school you may also order this on the <a href="http://it.stonybrook.edu/services/solar" target="_blank">SOLAR System</a>.</p>

