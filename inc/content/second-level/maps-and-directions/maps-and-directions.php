                <h2 id="directions" class="clearfix">Directions</h2>
                <h3>Traveling to Main Campus (Stony Brook, NY)</h3>
                <p class="clearfix">Stony Brook University is situated on an 1,039 acre site (see campus maps) on the north shore of Long Island in southeastern New York. We are approximately 60 miles east of New York City.</p>
                <br />

                <h4>By Car</h4>
                <p class="clearfix">From New York City, take the Long Island Expressway (LIE, I-495) eastbound from the Queens Midtown Tunnel in Manhattan or the Throgs Neck Bridge or Whitestone Bridge in Queens to exit 62, and follow Nicolls Road (Route 97) north for 9 miles. The main entrance to the University is on the left. To find the campus via GPS, enter the address "100 Nicolls Road, Stony Brook, NY 11790."</p>
                <p class="clearfix"><a href="https://www.e-zpassny.com/en/about/about.shtml" target="_blank"><img src="img/maps/ezpass.jpg" alt="EZ Pass Logo"> Save Time and Money on NYS Thruway Travel with E-ZPass</a></p>
                <p><a href="<?php echo $sbu_site_url['directions']['stony-brook']; ?>" class="brand-button external-link" target="_blank">Get Directions to Stony Brook</a></p>

                <br />
                <h4>By Train</h4>
                <p class="clearfix">Take the <a href="http://lirr42.mta.info/" target="_blank">Long Island Railroad</a>'s Port Jefferson line from Penn Station in Manhattan to Stony Brook. The LIRR station is at the north end of the campus; bus service to the central campus is provided. Trains to and from Penn generally require transfers at Jamaica or Huntington. Hicksville is also a transfer point on some lines.</p>

                <br />
                <h4>By Bus</h4>
                <p class="clearfix">
                    7Bus provides round-trip, seven-days-a-week transport between the Stony Brook campus and the Lower East Side of Manhattan and Rego Park, Queens. For more information, visit the <a href="http://7bus.com/" target="_blank">7Bus website.</a>
                </p>

                <br />
                <h4>By Ferry</h4>
                <p class="clearfix">Car ferries cross Long Island Sound at <a href="http://www.88844ferry.com/" target="_blank">Bridgeport, Connecticut to Port Jefferson, Long Island.</a> Take Route 25A west to Nicolls Road, and turn left on Nicolls. The University entrances are the first three intersections you come to.</p>
                <p class="clearfix">From <a href="https://www.longislandferry.com/" target="_blank">New London, Connecticut to Orient Point, Long Island,</a> take Route 25A west to Nicolls Road and proceed as above.</p>
                
                <h4>By Plane</h4>
                <p class="clearfix"><a href="http://www.macarthurairport.com/" target="_blank">Long Island's Islip-MacArthur Airport</a> is 16 miles from the campus and is serviced by direct flights by major airlines and commuter lines.</p>
                <p class="clearfix">New York City Area Airports (JFK, LaGuardia and Newark) are approximately 50 miles to the west. Fromâ€ˆKennedy Airport, you can take the <a href="http://web.mta.info/mta/airtrain.htm"  target="_blank">AirTrain</a> to Jamaica Station and transfer to the <a href="http://lirr42.mta.info/" target="_blank">Long Island Railroad</a> for the train to Stony Brook (take the Port Jefferson line).</p>
                <p class="clearfix"><strong>Driving directions to Stony Brook University from Islip-MacArthur Airport</strong>: Exit the airport and turn left onto Veterans Memorial Highway (Route 454 West). Merge onto Sunrise Highway (Route 27) and take the second exit onto Nicolls Road. Drive north for 12 miles. The main entrance to the University is on the left.</p>
                <br />
                <br />
