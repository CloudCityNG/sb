                <h3 id="southampton">Traveling to Southampton Campus</h3>
                <p class="clearfix"><strong>Stony Brook Southampton<br />
                239 Montauk Highway<br />
                Southampton, NY 11968</strong></p>
                <br />
                
                <h4>By Car</h4>
                <p class="clearfix">From New York City and other points west of Southampton, take I-495 (Long Island Expressway) east to Exit 70, then turn right on Route 111 (follow the signs for Route 27 East/Montauk - Sunrise Highway). Take Route 27 (east) and proceed 19 miles to Southampton Campus. Make right on Tuckahoe Road. Go past first entrance on Tuckahoe Road and past the empty field. Turn right at next entrance where you will see a guard booth on your right. On the left, a parking lot is available.</p>
