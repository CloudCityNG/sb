                <h3>Directions to the University</h3>
                <h4>Main Campus</h4>
                <p class="clearfix">Stony Brook University is situated on an 1,039 acre site (see campus maps) on the north shore of Long Island in southeastern New York. We are approximately 60 miles east of New York City.</p>

                <strong>By Car</strong>
                <p class="clearfix">From New York City, take the Long Island Expressway (LIE, I-495) eastbound from the Queens Midtown Tunnel in Manhattan or the Throgs Neck Bridge or Whitestone Bridge in Queens to exit 62, and follow Nicolls Road (Route 97) north for 9 miles. The main entrance to the University is on the left. To find the campus via GPS, enter the address "100 Nicolls Road, Stony Brook, NY 11790."</p>
                <p class="clearfix"><a href="https://www.e-zpassny.com/en/about/about.shtml"><img src="img/maps/ezpass.jpg" alt="EZ Pass Logo"> Save Time and Money on NYS Thruway Travel with E-ZPass</a></p>
                               <strong>By Train</strong>
                <p class="clearfix">Take the <a href="http://mta.info/lirr/">Long Island Railroad's</a> Port Jefferson line from Penn Station in Manhattan to Stony Brook. The LIRR station is at the north end of the campus; bus service to the central campus is provided. Trains to and from Penn generally require transfers at Jamaica or Huntington. Hicksville is also a transfer point on some lines. <a href="http://lirr42.mta.info/index.php?error=noRoute">Find schedule and fare information here.</a></p>

                <br />
                <strong>By Bus</strong>
                <p class="clearfix">
                    7Bus provides round-trip, seven-days-a-week transport between the Stony Brook campus and the Lower East Side of Manhattan and Rego Park, Queens. For more information, visit the <a href="http://7bus.com/">7Bus website.</a>
                </p>

                <br />
                <strong>By Ferry</strong>
                <p class="clearfix">Car ferries cross Long Island Sound at <a href="http://www.88844ferry.com/">Bridgeport,         Connecticut to Port Jefferson, Long Island.</a> Take Route 25A west to Nicolls Road, and turn left on Nicolls. The University entrances are the first three intersections you come to.</p>
                <p class="clearfix">From <a href="https://www.longislandferry.com/">New London, Connecticut to Orient Point, Long Island,</a> take Route 25A west to Nicolls Road and proceed as above.</p>

                
                <strong>By Plane</strong>
                <p class="clearfix"><a href="http://www.macarthurairport.com/">Long Island's Islip-MacArthur Airport</a> is 16 miles from the campus and is serviced by direct flights by major airlines and commuter lines.</p>
                <p class="clearfix"><a href="http://a.cms.omniupdate.com/10/">New York City Area Airports</a>(JFK, LaGuardia and Newark)are 50 miles to the west. Fromâ€ˆKennedy Airport, you may take the <a href="http://web.mta.info/mta/airtrain.htm">AirTrain</a> to Jamaica Station and transfer to the LIRR for the train to Stony Brook (Port Jefferson line).</p>
                <p class="clearfix">Driving directions to Stony Brook University from Islip-MacArthur Airport: Exit the airport and turn left onto Veterans Memorial Highway (Route 454 West). Merge onto Sunrise Highway (Route 27) and take the second exit onto Nicolls Road. Drive north for 12 miles. The main entrance to the University is on the left.</p>
                <br />

                <h4>Southhampton</h4>
                <p class="clearfix">Stony Brook Southampton is located at 239 Montauk Highway, Southampton, NY 11968.</p>
                
                <strong>By Car</strong>
                <p class="clearfix">From New York City and other points west of Southampton, take I-495 (Long Island Expressway) east to Exit 70, then turn right on Route 111 (follow the signs for Route 27 East/Montauk - Sunrise Highway). Take Route 27 (east) and proceed 19 miles to Southampton Campus. Make right on Tuckahoe Road. Go past first entrance on Tuckahoe Road and past the empty field. Turn right at next entrance where you will see a guard booth on your right. On the left, a parking lot is available 

                <br />
                <p class="clearfix"><strong class="redtext">By Train</strong><br />
                Take the <a href="http://www.mta.nyc.ny.us/lirr/html/ttn/lirrtt.htm" target="_blank">Long   Island Railroad's</a> Montauk Branch from Jamaica Station in New York City to   Southampton. (Service from Manhattan's Penn Station to Jamaica runs frequently.)   The campus is a short taxi ride from the station. Here is the <a href="http://www.mta.nyc.ny.us/lirr/html/ttn/southamp.htm" target="_blank">schedule for Southampton</a>.</p>

