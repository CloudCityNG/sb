                    <h4>Your privacy is our mandate</h4>
                    <p>The Stony Brook Foundation is committed to ensuring the privacy, confidentiality, and security of those who visit and make donations on our website. This statement describes our information collection practices and explains how we use and protect your personal information. This statement applies specifically to information collected by this website.</p>

                    <h4>Credit Card Information</h4>
                    <p>To ensure that credit card information is secure and electronic payments are processed safely and efficiently, we use a secured site to protect your online donation.</p>

                    <h4>Other Personally Identifiable Information</h4>
                    <p>To make an online gift, we require you to provide your full name, address, and email address.  You may also choose to provide additional information. You may elect to receive email from us and you may opt out of receiving email alerts by checking the appropriate box on any alert. This website will not sell or transfer such information to any third parties except (i) to the extent necessary to authorize third parties such as the secured site to process the transaction you are authorizing and to use the necessary information needed to perform their functions and (ii) on select occasions to authorized vendors for the purpose of offering services to alumni. In all such cases we require the third parties to agree not to sell or transfer the information. In addition, we may disclose personal information when we have a good-faith belief that disclosure is required by law. Stony Brook University and Stony Brook Foundation may use the information you provide to update your contact information and to maintain contact with our alumni.</p>

                    <h4>Refund Policy</h4>
                    <p>By making a charitable gift to the Stony Brook Foundation, a 501(c)(3) organization, the donor understands that charitable donations are not refundable.</p>

                    <h4>Publication of Donor Names</h4>
                    <p>The names of all donors may be included in materials such as honor rolls, annual reports, and other appropriate publications, in both print and electronic formats, unless otherwise requested by the donor. Donors who have made gifts to the Foundation via bequest or other testamentary device are deemed to have granted such permission unless otherwise specified in the gift instrument or other document.</p>

                    <h4>Links to Other Sites</h4>
                    <p>This site contains links to other sites. We are not responsible for the privacy practices or the content of such websites. We encourage you to be aware of the privacy policies of such sites.</p>

                    <h4>Updates to This Privacy Policy</h4>
                    <p>We may make changes or additions to this policy at any time and will post the changes on this web page.</p>

                    <h4>Questions</h4>
                    <p>If you have any questions about this privacy statement, the practices of this site, or your dealings with this website, please contact <a class="email-link" href="mailto:advancement@stonybrook.edu">advancement@stonybrook.edu</a> or call Kathleen Weber at (631) 632-4887.</p>
                    <p>Your use of this site constitutes your understanding of, and agreement to, the terms of this policy.</p>

