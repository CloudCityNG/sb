                <h4>How to Apply: Prospective External Candidates</h4>
                <p>Stony Brook employs more than 14,000 staff who supports our mission, vision and values in a vast array of significant ways.</p>

                <h5>Step 1: Search</h5>
                <p>You can filter your job search by job field, organization, job location, keyword or job schedule. Note that is it possible to select multiple items in some fields. Select the Job Title to review the full posting.</p>
                <p>To explore more about employment at a specific location, visit one of the following pages:</p>
                <ul class="arrow-bullets red-before-children">
                    <li><a href="http://stonybrook.edu/hr/prospective-employees.shtml#career-opportunities" class="card"><span>Jobs @ Stony Brook University</span></a></li>
                    <li><a href="http://stonybrookmedicine.edu/careers" class="card"><span>Jobs @ Stony Brook Medicine</span></a></li>
                    <li><a href="http://www.listateveteranshome.org/employment_opportunities.html" class="card"><span>Jobs @ Long Island State Veterans Home</span></a></li>
                    <li><a href="https://stonybrooku.taleo.net/careersection/2/jobsearch.ftl?lang=en" class="link"><span>All Jobs</span></a></li>
                </ul>

                <h5>Step 2: Apply Online</h5>
                <p>Select <strong>Apply Online</strong> to begin the application process. New users can create an account by selecting <strong>Register as a New User</strong>. The application requires that you provide contact information, salary requirements and work eligibility information, and requests demographic/race/ethnicity information.</p>

                <h5>Step 3: Submit Resume</h5>
                <p>A resume is required and can be uploaded or manually developed in our online application system. Acceptable formats for uploading a resume are Word (.doc) or PDF (.pdf) files. Scanned resumes and other formats are not acceptable and will not be read by our system.</p>

                <h5>Step 4: Submit Cover Letter</h5>
                <p>Cover letters are recommended. However, they are not required if they are not indicated in the posting. To include a cover letter, you must upload a Word (.doc) or PDF (.pdf) file at the attachment step of the application.</p>

                <h5>Next Steps</h5>
                <p>Once you've E-signed your application and pressed the <strong>SUBMIT</strong> button, you will immediately receive an automated email confirming receipt of your application. A recruiter or search committee will have access to your application materials immediately. If you are considered for a position, you will hear from them directly. Otherwise, you may not hear from us until the selection process is complete and all candidates have been dispositioned.</p>

                <h5>Additional Information</h5>
                <p>Our system does not allow for you to track the progress of your application or your candidacy for a position. If you have the need to modify your original application, you may login to modify the submission if the job posting is still active.</p>
                <p>To view all of your submissions, select the <strong>My Jobpage</strong> tab. Incomplete submissions will not be considered.</p>
                <p>You may select the option in “Basic Profile Information” to receive an email notification whenever a new position matching your profile is posted.</p>

                <p><a class="sbu-cta white sbu-cta-2 sbu-cta-1a" href="https://stonybrooku.taleo.net/careersection/2/jobsearch.ftl?lang=en">View All Job Postings</a></p>

