                <h5>Undergraduate Colleges</h5>
                <p>Each Undergraduate College has assigned advisors  who help first-year students make good decisions about a wide range of topics relevant to student academic success. Advising services are available through Advising Walk-in Hours, Individual Advising Appointments and through Freshman Seminar 101, which informs first-year students of deadlines and policies, and gives them the tools to self-manage course selection.</p>
                <p><strong>Location:</strong> N-3071 Melville Library<br />
                <strong>Phone:</strong> (631) 632-4378</p>
                <p><a class="external-link" href="http://ucolleges.stonybrook.edu/advising" target="_blank">View the Undergraduate Colleges</a></p>

                <h5>University Scholars</h5>
                <p>Provides primary advising support for first year students in the University Scholars Program from summer orientation through the end of the second semester at Stony Brook. Students can also see their Undergraduate College advisor, or, if appropriate, advisors from Athletics or Engineering.</p>
                <p><strong>Location:</strong> N-3071 Melville Library<br />
                <strong>Phone:</strong> (631) 632-4378</p>
                <p><a class="external-link" href="http://ucolleges.stonybrook.edu/advising" target="_blank">Learn More about University Scholars</a></p>

