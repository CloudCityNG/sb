            <h3>Academic Advising</h3>
            <div class="content">
                <p>Stony Brook University offers a full complement of academic advising resources for undergraduates. Support includes advising about general education, major, and minor degree requirements, as well as comprehensive communication aimed at promoting student success.</p>
            </div>
            <div class="content col-2">
                <h4>Major Advising</h4>

                <h5>College of Arts and Sciences</h5>
                <p>The College of Arts and Sciences includes 36 majors and 39 minors. Each academic department has a designated Undergraduate Program Director who coordinates advising services for students in their major(s) or minor(s). Students can <a class="external-link" href="http://www.stonybrook.edu/healthed/" target="_blank">contact Undergraduate Program Directors</a> and should make an appointment to discuss or declare a major no later than the middle of the sophomore year.</p>
                <p><a class="external-link" href="http://www.stonybrook.edu/cas" target="_blank">Visit the College of Arts and Sciences</a></p>

                <h5>College of Business</h5>
                <p>The Office of Student Services (OSS) is the main academic advising center for the College of Business. The office advises business majors, minors, and MBA students regarding course and specialization selection.The office also advises prospective MBA students regarding the admission application process.</p>
                <p><strong>Location:</strong> 102 Harriman Hall<br />
                <strong>Phone:</strong> (631) 632-7171<br />
                <strong>Email:</strong> <a href="mailto:oss@notes.cc.sunysb.edu">oss@notes.cc.sunysb.edu</a></p>
                <p><a class="external-link" href="http://www.stonybrookcob.com/" target="_blank">Visit the College of Business</a></p>

                <h5>College of Engineering &amp; Applied Sciences</h5>
                <p>Students in the College of Engineering and Applied Sciences are required to meet regularly with their faculty advisors to ensure they are making progress in their majors. Students may find their faculty advisors by visiting their department office or the Undergraduate Student Office.</p>
                <p><strong>Location:</strong> 127 Engineering<br />
                <strong>Phone:</strong> (631) 632-8381</p>
                <p><a class="external-link" href="http://www.ceas.sunysb.edu/" target="_blank">Visit the College of Engineering &amp; Applied Sciences</a></p>

                <h5>Health Sciences Programs</h5>
                <p>The Health Sciences Schools include a wide array of opportunities for students at the undergraduate and graduate levels. Each school holds information sessions regarding their programs and individual advisement by appointment. For contact information, please click on the appropriate school:</p>
                <p><a class="external-link" href="http://healthtechnology.stonybrookmedicine.edu/" target="_blank">Health Technology and Management</a><br />
                <a class="external-link" href="http://www.nursing.stonybrookmedicine.edu/" target="_blank">Nursing</a><br />
                <a class="external-link" href="http://www.hsc.stonybrook.edu/ssw/index.cfm" target="_blank">Social Welfare</a></p>
                <p><strong>Location:</strong> Office of Student Services, HSC Level 2, Room 271</p>
                <p><a class="external-link" href="http://www.stonybrook.edu/hscstudents/" target="_blank">View More Information on Health Sciences Programs</a></p>

                <h5>School of Journalism</h5>
                <p>The School of Journalism provides major academic advising to all students interested in the journalism major or minor.To declare the major or minor and review degree requirements, students should call for an advising appointment.</p>
                <p><strong>Location:</strong> N-4004 Melville Library<br />
                <strong>Phone:</strong> (631) 632-7403</p>
                <p><a class="external-link" href="http://www.stonybrook.edu/journalism" target="_blank">Visit the School of Journalism</a></p>

                <h5>School of Marine and Atmospheric Sciences</h5>
                <p>The School of Marine and Atmospheric Sciences provides major academic advising to all SoMAS students. Faculty members noted below are available to discuss transfer course evaluation, progress toward degree and course selection as well as general questions about careers, research, and internship opportunities.</p>
                <p><strong>Stony Brook West Campus:</strong> Prof. Kamazima Lwiza, Director of Undergraduate Programs, Endeavour Hall, Room 169, (631) 632-7309</p>
                <p><strong>Stony Brook Southampton Campus:</strong> Christopher Gobler, Natural Sciences, Room 125, (631) 632-5043</p>
                <p><a class="external-link" href="http://www.somas.stonybrook.edu/education/" target="_blank">Visit the School of Marine and Atmospheric Sciences</a></p>

                <h5>Sustainability Studies</h5>
                <p>The Sustainability Studies program offers five majors and six minors. Each program has a designated Undergraduate program Director who advises on the major and minor. Students can contact the faculty directors by visiting the Sustainability Studies website. The faculty advisors provide ongoing assistance to students in developing their academic planning toward graduation.</p>
                <p><strong>Location:</strong> W-0511 Melville Library<br />
                <strong>Phone:</strong> (631) 632-9404</p>
                <p><a class="external-link" href="http://www.stonybrook.edu/commcms/sustainability/index.html" target="_blank">View the Sustainability Studies Program</a></p>

            </div>

