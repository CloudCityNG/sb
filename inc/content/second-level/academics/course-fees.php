                <h3>Course Fees</h3>
                <img class="inline-image" src="img/university/pole-banners-far-beyond.jpg" />
                <p>In accordance with SUNY policy regarding Fees, Rentals, and Other Charges, Stony Brook University charges the following course-based fees to offset extraordinary costs such as specialized service, specialized supplies and materials, purchasing or maintaining specialized equipment, testing, clinical and practicum activities.</p>
                <p>Course fees are associated either with specific courses or based on student status or credit load.  Although the University will make every attempt to maintain accurate course fee information, the specific courses and associated fees are subject to change without notice.</p>
                <p>Course fees are also collected for students in Dental and Medical programs, as specified by the <a href="http://www.stonybrook.edu/bursar/tuition/">Bursar's office</a>.</p>
                <p><em>See the color-coded keys below for applicable notes.</em></p>
