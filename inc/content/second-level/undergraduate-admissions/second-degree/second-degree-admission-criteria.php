            <h3>Pursuing a Second Degree</h3>
            <div class="content clearfix">
                <h4>Applying for a Second Bachelor's Degree</h4>
                <p>The following restrictions apply to Stony Brook University’s Second Bachelor Degree Program.</p>
                <ul class="default-bullets">
                    <li>A second bachelor's degree may only be earned in a discipline that is significantly different from the first bachelor's degree.</li>
                    <li>Admission to our Second Bachelor’s Degree Program is extremely limited due to a lack of capacity in most of our undergraduate programs and second-degree candidates are typically not admitted to our Civil Engineering, Computer Science, or Information Systems majors.</li>
                    <li>Priority for admission to the University is given to first-degree candidates. We will not begin notifying fall applicants to our Second Bachelor’s Degree Program of admission decisions until August, after first-degree applicants are notified and we have determined what, if any, capacity remains in our undergraduate programs.</li>
                    <li>We reserve the right to restrict enrollment to only those courses required for the second bachelor's degree.</li>
                    <li>Those admitted to our Second Bachelor's Degree Program may not change their major without the approval of the Dean of Admissions.</li>
                </ul>
                <p>Due to the restrictions* noted above, we recommend the exploration of options offered at other institutions to obtain the desired education.</p>
                <p>Those who elect to apply to Stony Brook University for a second bachelor's degree are required to:</p>
                <ol class="number">
                    <li><a href="undergraduate-admissions/apply/transfer/application-procedures/">Apply as a transfer student</a></li>
                    <li>Submit official copies of transcripts from all colleges and universities attended</li>
                    <li>Have a minimum cumulative grade point average of 3.0 in all post-secondary studies</li>
                </ol>
                <p>Prior to the start of classes, a final transcript from the institution awarding the first bachelor’s degree must be submitted, indicating the previous degree earned on the official transcript.</p>
                <p>Students accepted to the program must earn at least 36 credits in residence at Stony Brook, including completion of a new major and selected general education requirements.</p>
                <p><em>*Second bachelor degree applicants for Athletic Training, Clinical Laboratory Sciences, Nursing, Respiratory Care, or Social Work, should contact the <a class="external-link" href="http://www.stonybrook.edu/hscstudents/" target="_blank">Health Sciences Center</a> for restrictions and application procedures.</em></p>
