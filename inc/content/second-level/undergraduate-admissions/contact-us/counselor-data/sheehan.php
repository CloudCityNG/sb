    <p>Donnie joined the Stony Brook University Admissions team in August 2012 as an Admissions Counselor. After earning his Bachelor&rsquo;s Degree in Sociology from Penn State University, Donnie happily returned to his childhood home of Long Island to attend Stony Brook University&rsquo;s School of Social Welfare, where he received his Masters in Social Work specializing in Student Community Development.</p>
    <p>As a social worker, Donnie advocates for respective communities and works towards improving the quality of life for those that encompass it. He seeks to help students find a positive and rewarding experience in which they gain not only a better understanding of themselves, but a better understanding of their effect on the community around them.<br />
      <br />
      Donnie relishes visiting the eclectic high schools of Queens and Bronx, New York. He especially enjoys having conversations with students about their passions and aspirations. He promotes all prospective students to visit Stony Brook&rsquo;s beautiful campus, where you may find him napping on the Staller Steps.<br />
