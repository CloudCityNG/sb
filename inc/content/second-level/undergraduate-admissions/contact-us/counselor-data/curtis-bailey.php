<div class="content">
    <p>Michelle joined the Stony Brook University Admissions team in August 2012 as Senior Admissions Advisor and EOP Coordinator. As a self-proclaimed, product of an invaluable SUNY education, she holds both a B.A. in Sociology and M.S. in Student Personnel Administration from SUNY Buffalo State. With previous professional experience in Undergraduate Admissions, Residence Life and Student Life, Michelle continues to move forward ensuring that students have access to quality higher education.</p>
    <p>Having personally experienced both National Student Exchange (North Carolina) and Study Abroad (Salamanca, Spain), Michelle encourages all students to make time to craft one of these experiences into their college experience so they can benefit from the educational and cultural exchange these opportunities can afford them.</p>
    <p>As a native of Brooklyn, NY, Michelle spends balanced time recruiting quality students from New York City to Stony Brook University as well as giving back in her local communities by serving as an advocate to ensure that students and parents understand the ins and outs of the College Admissions Process.</p>
