    <p>Dina has been an Admissions Counselor at Stony Brook University since 2002, and in 2005 she received her Master’s in Professional Studies. She directs both the Accelerated College Education Program for high school students, and the Advanced Placement Summer Institutes for high school teachers.</p>
    <p>An advocate for getting involved, she has been a member of Stony Brook’s Student Affairs Professional Development Committee, the Summer Celebration Committee, and the Outstanding Service Awards Committee. Off-campus, she enjoys being involved with the Western Suffolk Counselors Association, where she has served on the Merit Awards Committee.</p>
    <p>Dina's favorite thing about SBU is attending the 4.0 Awards Ceremony with students like Andrew Mann who has a 4.0 GPA, is a mechanical engineering major, a member of the Honors College and a drummer in the marching band.</p>
