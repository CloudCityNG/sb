<div class="content">
    <p>Gerome is a proud Stony Brook University graduate, earning a Master's in Social Work with a specialization in Student Community Development. As a licensed Social Worker and first generation college graduate, Gerome knows that the college selection process can be arduous, and enjoys helping prospective students and their families navigate through the process.</p>
    <p>Gerome loves traveling and is always impressed by the talented and diverse pool of prospective students that Stony Brook attracts. Among Gerome's many roles in the office include recruiting potential transfer students. Having taught the Transfer Seminar Course (ADV 101), Gerome enjoys helping transfer students transition into the campus community.</p>
    <p>Gerome's advice for students is to try to be purposeful in their academic and social endeavors by taking advantage of the bevy of resources available to them at Stony Brook and to be self advocates. Gerome is always reminding students that, while he can provide guidance and direction, they must show initiative and personal fortitude to be successful.</p>
