            <div class="content">
                <p>If you are in or plan to enter the United States in another non-immigrant status, you may or may not need to obtain F-1 or J-1 student status in order to study at Stony Brook University. For example, most dependents (such as H-4 dependents) are permitted to study while accompanying their family members in the U.S. However, many non-immigrant statuses carry immigration restrictions on employment that may make it desirable to seek F-1 status. Some typical situations include the following:</p>
                <ol class="number">
                    <li>The following immigration classifications are not permitted to study or to lawfully accept employment while in the U.S.:
                        <ul>
                            <li>Tourists (individuals admitted in WT / WB or B-1 / B-2 status) are not eligible to study while in the United States, and must obtain F-1 status to study.</li>
                            <li>F-2 dependents are not permitted to pursue academic study and must switch to F-1 status.</li>
                        </ul>
                    </li>
                    <li>The following immigration classifications are permitted to study, but may not lawfully accept employment while in the U.S.:
                        <ul>
                            <li>H-4 dependents of H-1 temporary workers</li>
                            <li>TD dependents of TN (NAFTA) workers</li>
                        </ul>
                    </li>
                    <li>The following immigration classifications are permitted to study full or part-time, and may apply for employment authorization incident to their status:
                        <ul>
                            <li>Dependents in E-2 or L-2 status</li>
                            <li>Dependents in G-4 status</li>
                            <li>Dependent spouses in J-2 status</li>
                        </ul>
                    </li>
                </ol>
                <p>If you are the primary visa holder and your immigration status depends on employment (as a A-1 diplomat, H-1 temporary specialty occupation worker, G-4 international organization employee, etc.), you will need to switch to F-1 or J-1 status if you decide to terminate your employment. Otherwise, in most cases you may study 'incident to status,' as long as you continue to fulfill the terms and obligations or your current immigration status.</p>

