            <h3>Next Steps for Entering Freshman</h3>
            <div class="content">
                <p>As an entering freshman student to Stony Brook, here's what you need to do next to reserve your space at Stony Brook.</p>
                <h4>Step 1: Make Your Deposits and Complete New Student Preferences</h4>
                <p>Pay your deposits and fee, and complete your New Student Preferences by logging on to Stony Brook’s secure <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR System</a>, using your <strong>Stony Brook ID number</strong> and <strong>password</strong>. <em>Your ID number and your original password are printed on the upper right of your letter of admission. If you logged into the SOLAR System in the past, you were already prompted to change your password. If you do not remember your password, go to the SOLAR login page, select "SOLAR Password Help," and follow the instructions to reset it.</em></p>
                <p>If you prefer to mail your payments, you may print out PDF’s of the Admission Acceptance/Deposit Form and Orientation Fee Payment Form.</p>
                <ul class="default-bullets">
                    <li><a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/DepositForm.pdf" target="_blank">Admission Acceptance/Deposit Form</a></li>
                    <li><a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/OrientationForm.pdf" target="_blank">Orientation Fee Payment Form</a></li>
                </ul>
                <p>Here is a list of the deposits:</p>
                <ul class="arrow-bullets">
                    <li><strong>$150 Tuition Deposit.</strong> Your tuition deposit must be received by the date indicated on your letter of admission.</li>
                    <li><strong>$200 Campus Housing Deposit.</strong> All new freshmen are guaranteed housing provided the tuition/housing deposit is received and their new student preferences are submitted online by May 1.</li>
                    <li><strong>$300 Orientation Fee.</strong> Orientation is mandatory for all new students.</li>
                </ul>
                <p><em>For additional information about <a class="external-link" href="http://www.stonybrook.edu/bursar/policy.shtml" target="_blank">billing</a> and <a class="external-link" href="http://www.stonybrook.edu/bursar/withdrawals.shtml#refunds" target="_blank">refund</a> policies, please visit <a class="external-link" href="http://www.stonybrook.edu/bursar/" target="_blank">Bursar/Student Accounts</a>.</em></p>
                <p>While you are logged into SOLAR, <strong>complete your New Student Preferences.</strong> Tell us your preferences for <a class="external-link" href="http://ucolleges.stonybrook.edu/new-student" target="_blank">Undergraduate Colleges</a>, Orientation, campus housing, meal plans, and more. All new students are required to submit their New Student Preferences online by May 1.</p>

                <h4>Step 2: Accept Financial Aid Awards</h4>
                <p>Click on "Financial Aid" in <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR</a> to accept/decline your awards online.</p>

                <h4>Step 3: Complete Forms</h4>
                <p>Complete and submit these forms:</p>
                <ol class="number">
                    <li>
                        <strong>Health Forms.</strong>
                        <ul class="default-bullets">
                            <li>The <a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/ImmunizationForm.pdf" target="_blank">Immunization Form</a> must be submitted at least two weeks prior to your Orientation date.</li>
                            <li>The <a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/MeningitisForm.pdf" target="_blank">Meningitis Form</a> must be submitted at least two weeks prior to your Orientation date. If you are 18 years of age or older, this requirement can be met by logging on to your SOLAR account and submitting your response electronically under "Holds."</li>
                            <li>The <a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/Health.PDF" target="_blank">Health Form</a> must be submitted before the first day of classes.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Disability Dervices Form </strong>(optional).
                        <ul class="default-bullets">
                            <li>If you are a student with a disability that may impact your coursework, complete the <a href="http://www.stonybrook.edu/ugadmissions/admitted/forms/DisabilitySupportForm.pdf" target="_blank">Voluntary Disclosure Form</a>.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Advising Planning Form.</strong>
                        <ul class="default-bullets">
                            <li>After receiving your orientation date, login to SOLAR to complete the Advising Planning Form. In this form, you will be asked questions about your academic interests, AP tests and scores, and transfer credits. You will also sign up for your proctored Math Assessment and receive information about Writing Placement.</li>
                            <li>This information will be used by your advisors to suggest appropriate courses when you register for classes at orientation. You must complete the Advising Planning Form.</li>
                        </ul>
                    </li>
                </ol>

