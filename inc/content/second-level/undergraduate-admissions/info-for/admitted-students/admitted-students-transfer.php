            <h3>Next Steps for Transfer Students</h3>
            <div class="content">
                <p>As a student transferring to Stony Brook, here's what you need to do next in the enrollment process.</p>
                <h4>Step 1: Make Your Deposits and Complete New Student Preferences</h4>
                <p>Pay your deposits and fee, and complete your New Student Preferences by logging on to Stony Brook’s secure <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR System</a>, using your <strong>Stony Brook ID number</strong> and <strong>password</strong>. <em>Your ID number and your original password are printed on the upper right of your letter of admission. If you logged into the SOLAR System in the past, you were already prompted to change your password. If you do not remember your password, go to the SOLAR login page, select "SOLAR Password Help," and follow the instructions to reset it.</em></p>
                <p>If you prefer to mail your payments, you may print out PDF’s of the Admission Acceptance/Deposit Form and Orientation Fee Payment Form.</p>
                <ul class="default-bullets">
                    <li><a href="downloads/pdf/admissions/DepositForm.pdf" target="_blank">Admission Acceptance/Deposit Form</a></li>
                    <li><a href="downloads/pdf/admissions/OrientationForm.pdf" target="_blank">Orientation Fee Payment Form</a></li>
                </ul>
                <p>Here is a list of the deposits:</p>
                <ul class="arrow-bullets">
                    <li><strong>$150 Tuition Deposit.</strong> Your tuition deposit must be received by the date indicated on your letter of admission.</li>
                    <!--<li><strong>$200 Campus Housing Deposit.</strong> All new freshmen are guaranteed housing provided the tuition/housing deposit is received and their new student preferences are submitted online by May 1.</li>-->
                    <li><strong>$300 Orientation Fee.</strong> Orientation is mandatory for all new students.</li>
                </ul>
                <p><em>For additional information about <a class="external-link" href="http://www.stonybrook.edu/bursar/policy.shtml" target="_blank">billing</a> and <a class="external-link" href="http://www.stonybrook.edu/bursar/withdrawals.shtml#refunds" target="_blank">refund</a> policies, please visit <a class="external-link" href="http://www.stonybrook.edu/bursar/" target="_blank">Bursar/Student Accounts</a>.</em></p>
                <p>While you are logged into SOLAR, <strong>complete your New Student Preferences.</strong> Tell us your preferences for <a class="external-link" href="http://ucolleges.stonybrook.edu/new-student" target="_blank">Undergraduate Colleges</a>, Orientation, campus housing, meal plans, and more. All new students are required to submit their New Student Preferences online.</p>
                <p><em>Note: You will not receive your Orientation date, at which time you will register for classes, until you submit your New Student Preferences.</em></p>

                <h4>Step 2: Accept Financial Aid Awards</h4>
                <p>Click on "Financial Aid" in <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR</a> to accept/decline your awards online.</p>

                <h4>Step 3: Complete Forms</h4>
                <p>Complete and submit these forms:</p>
                <ol class="number">
                    <li>
                        <strong>Health Forms.</strong>
                        <ul class="default-bullets">
                            <li>The <a href="downloads/pdf/admissions/ImmunizationForm.pdf" target="_blank">Immunization Form</a> must be submitted at least two weeks prior to your Orientation date.</li>
                            <li>The <a href="downloads/pdf/admissions/MeningitisForm.pdf" target="_blank">Meningitis Form</a> must be submitted at least two weeks prior to your Orientation date. If you are 18 years of age or older, this requirement can be met by logging on to your SOLAR account and submitting your response electronically under "Holds."</li>
                            <li>The <a href="downloads/pdf/admissions/Health.pdf" target="_blank">Health Form</a> must be submitted before the first day of classes.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Disability Dervices Form </strong>(optional).
                        <ul class="default-bullets">
                            <li>If you are a student with a disability that may impact your coursework, complete the <a href="downloads/pdf/admissions/DisabilitySupportForm.pdf" target="_blank">Voluntary Disclosure Form</a>.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Advising Planning Form.</strong>
                        <ul class="default-bullets">
                            <li>After receiving your Orientation date, login to SOLAR to complete this form to sign up for a <a class="external-link" href="http://assess.math.sunysb.edu/" target="_blank">math assessment</a>. This must be completed before attending orientation.</li>
                        </ul>
                    </li>
                </ol>

                <h4>Step 4: Final Steps</h4>
                <ol class="number">
                    <li>
                        To transfer your credits to Stony Brook, we must receive final official transcripts from all colleges and universities you have attended. Also, you must provide proof of high school graduation by sending us either a copy of your diploma or your high school transcript (noting date of graduation). If you have earned an associate’s degree, be sure that it’s posted on the transcript. To receive credit for AP courses taken in high school, you must have your AP scores sent to this office as well.
                        <ul class="default-bullets">
                            <li class="address">
                            Undergraduate Admissions<br />
                            118 Administration Building<br />
                            Stony Brook University<br />
                            Stony Brook, NY 11794-1901<br />
                            U.S.A.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Attend Orientation.</strong>
                        <ul class="default-bullets">
                            <li>Get ready for your enrollment by attending <a class="external-link" href="http://studentaffairs.stonybrook.edu/orientation/index.shtml" target="_blank">Orientation (Part 1)</a>. You’ll meet other students in your Undergraduate College, tour the University, meet with your Academic Advisor and register for classes. Orientation is mandatory for all new students.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Pay health insurance or apply for a waiver.</strong>
                        <ul class="default-bullets">
                            <li>Stony Brook requires all full time students to have comprehensive health insurance, and provides a <a class="external-link" href="http://studentaffairs.stonybrook.edu/shs/" target="_blank">plan for purchase</a> by those who do not currently have such a plan. If you already have health insurance, you must apply for a health insurance waiver or you will be automatically billed for SB's plan. Once you register for classes, a fee for health insurance will be posted on your account. Log on to SOLAR and look for insurance waivers under "Student Financial Services" on the menu bar.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Experience Stony Brook.</strong>
                        <ul class="default-bullets">
                            <li>Just a few days before the start of classes you will participate in Experience Stony Brook (Part 2 of Orientation), where you will discover the vast resources and opportunities available to you as an SB student. You’ll attend various programs and workshops focused on your academic success, campus involvement, and health and wellness.</li>
                        </ul>
                    </li>
                </ol>

