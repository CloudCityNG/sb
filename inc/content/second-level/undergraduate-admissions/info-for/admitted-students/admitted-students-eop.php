                <p>Congratulations on your admission to Stony Brook's Educational Opportunity Program/Advancement on Individual Merit (EOP/AIM)!</p>
                <p>Here's what you need to do next to reserve your space at Stony Brook.</p>
                <h4>Step 1: Accept Our Offer of Admission and Tell Us Your Preferences</h4>
                <ol class="number">
                    <li>
                        <strong>Accept Our Offer of Admission.</strong>
                        <ul class="default-bullets">
                            <li>Log onto Stony Brook’s secure <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR System</a>, using your <strong>Stony Brook ID number</strong> and <strong>password</strong>. Click on "EOP/AIM Accept/Decline" and accept our offer by the date indicated on your letter of admission. If we do not receive your response by this date, you will lose your space in our class. EOP/AIM students are not required to pay tuition and housing deposits, or the Orientation fee.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Complete your New Student Preferences by May 1.</strong>
                        <ul class="default-bullets">
                            <li>Tell us your preferences for <a class="external-link" href="http://ucolleges.stonybrook.edu/new-student" target="_blank">Undergraduate Colleges</a>, Orientation, campus housing*, meal plans, and more. All new students are required to submit their New Student Preferences online by May 1.</li>
                            <li>Note: You will not be able to register for classes or receive a housing assignment until you have completed your New Student Preferences.</li>
                        </ul>
                    </li>
                </ol>
                <p><em>*All new EOP/AIM students are guaranteed housing. To offer as many students as possible the experience of our residential learning environment, strategies that include assigning three students to a double room and other options will be utilized.</em></p>

                <h4>Step 2: Accept Financial Aid Awards</h4>
                <p>Click on "Financial Aid" in <a href="http://it.stonybrook.edu/services/solar" class="external-link" target="_blank">SOLAR</a> to accept/decline your awards online.</p>

                <h4>Step 3: Complete Forms</h4>
                <p>Complete and submit these forms:</p>
                <ol class="number">
                    <li>
                        <strong>Health Forms.</strong>
                        <ul class="default-bullets">
                            <li>The <a href="downloads/pdf/admissions/ImmunizationForm.pdf" target="_blank">Immunization Form</a> must be submitted at least two weeks prior to your Orientation date.</li>
                            <li>The <a href="downloads/pdf/admissions/MeningitisForm.pdf" target="_blank">Meningitis Form</a> must be submitted at least two weeks prior to your Orientation date. If you are 18 years of age or older, this requirement can be met by logging on to your SOLAR account and submitting your response electronically under "Holds."</li>
                            <li>The <a href="downloads/pdf/admissions/Health.PDF" target="_blank">Health Form</a> must be submitted before the first day of classes.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Disability Dervices Form </strong>(optional).
                        <ul class="default-bullets">
                            <li>If you are a student with a disability that may impact your coursework, complete the <a href="downloads/pdf/admissions/DisabilitySupportForm.pdf" target="_blank">Voluntary Disability Disclosure Form</a>.</li>
                        </ul>
                    </li>
                </ol>

