                <ol class="number">
                    <li>
                        <p><strong>Send your final, official transcripts and AP score reports</strong> as soon as they are available:</p>
                        <p class="address">
                            Undergraduate Admissions<br />
                            118 Administration Building<br />
                            Stony Brook University<br />
                            Stony Brook, NY 11794-1901<br />
                            U.S.A.
                        </p>
                    </li>
                    <li>
                        <strong>Attend Orientation.</strong>
                        <ul class="default-bullets">
                            <li>Get ready for your enrollment by attending <a class="external-link" href="http://studentaffairs.stonybrook.edu/orientation/index.shtml" target="_blank">Orientation (Part 1)</a>. You’ll meet other students in your Undergraduate College, tour the University, meet with your Academic Advisor and register for classes. Orientation is mandatory for all new students.</li>
                        </ul>
                    </li>
                    <li>
                        <strong>Freshmen: Attend EOP/AIM Pre-Freshman Summer Program</strong>
                        <ul class="default-bullets">
