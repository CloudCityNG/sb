            <h3>Home-Schooled Applicant Guidelines</h3>
            <div class="content">
                <h4>The Application Process</h4>
                <p>Stony Brook University is happy to accept and review applications from students who have been home-schooled. Home-schooled students comprise a small yet growing percentage of our applicant pool. Several home-schooled students are admitted and enroll at Stony Brook each year.
We prefer home-schooled applicants to submit the Common Application, as well as the Common Application’s Home School Supplement. However, if you choose to submit the SUNY application and SUNY supplement instead, we ask that you also submit a personal statement describing:</p>
                <ul class="default-bullets">
                    <li>how and why your family chose home schooling</li>
                    <li>how your learning process was organized</li>
                    <li>what benefits accrued</li>
                    <li>what, if any, choices you had to make to accomplish this type of education.</li>
                </ul>

                <h4>Academic Record</h4>
                <p>Home-schooled applicants are required to provide a copy of the approved Individualized Home Instruction Plan (IHIP) for each year of high school home schooling, to include:</p>
                <ul class="default-bullets">
                    <li>The child’s name, age and approximate grade level;</li>
                    <li>List of courses taken (these must comply with the minimum course requirements listed in Section 100.10 of the Commissioner’s Regulations);</li>
                    <li>Copies of quarterly reports to the local school district;</li>
                    <li>The annual assessment in accordance with Section 100.10;</li>
                    <li>A list of all Regents exams taken, the dates on which they were taken, and the scores earned, to be provided by the public school district on school letterhead. Stony Brook encourages New York State home-schooled students to take Regents exams.</li>
                </ul>
                <p>Many home-school applicants choose to challenge themselves by taking courses such as calculus, physics, philosophy, or foreign languages at a local college. Those residing in Suffolk County may wish to apply for admission to Stony Brook University’s Young Scholars Program for the spring of their junior year or fall of their senior year.</p>
                <p>If home schooling has been supplemented by formal coursework either at the secondary school or post-secondary level, official transcripts from the schools or colleges are required.</p>

                <h4>Testing</h4>
                <p>Most universities require submission of SAT or ACT scores. In evaluating SAT scores, many colleges place greater emphasis on the Math and Critical Reading sections. Scores on the Writing section are expected to receive more attention in the future.</p>

                <h4>Supplemental Application</h4>
                <p>With little other quantitative information available, standardized test scores (SAT Reasoning Test or ACT) for home schooled applicants may take on more significance than they might for our other applicants. We require the SAT Reasoning Test or the ACT, and strongly recommend SAT Subject Tests in some of the core academic subject areas (Math, Lab Sciences, English, etc.). Because each home-school program is different, SAT Subject Tests provide the committee with an additional standardized measure of your ability in these different areas. All scores should be sent directly from the testing agency to the University.</p>
                <p>Home-school applicants who have taken the <a class="external-link" href="http://www.tasctest.com/" target="_blank">Test Assessing Secondary Completion (TASC™)</a> are asked to provide a score report and the diploma itself (when available).</p>

                <h4>Evaluations</h4>
                <p>We require one evaluation from a teacher or counselor. Your parents and/or your primary instructor(s) can write this evaluation. If you have taken a college course or two during the high school years or have had an instructor outside your family as part of your home-schooling program, those teachers can also write this evaluation.</p>

                <p><strong>Special Requirement for Students of Compulsory School Age</strong></p> 
                <ul class="default-bullets">
                    <li>Section 3.47(b) of the Rules of the Board of Regents establishes a preliminary requirement for enrollment, applicable to students of compulsory school age who seek to meet compulsory educational requirements through full-time college study. <strong>Prior to enrolling, such student must submit to the degree-granting institution a valid and in-effect Individualized Home Instruction Plan (IHIP) verifying that the compulsory education requirements will be met through full-time college study. The IHIP must identify the degree-granting institution and the subjects to be covered by that study.</strong></li>
                    <li>This requirement does not apply to students of compulsory school age who are in attendance at college part-time or during the summer if the student is taking fewer than 12 semester hours or its equivalent in the summer semester.</li>
                </ul>
                <p>Please note that the application for admission is not reviewed until all required documentation is received.</p>
