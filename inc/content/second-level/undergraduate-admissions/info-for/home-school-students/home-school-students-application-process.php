            <h3>Accepted and Planning to Attend</h3>
            <div class="content">
                <h4>Documentation</h4>
                <p>Home-schooled students must provide documentation of successful completion of the high school home school program. This can be done by one of these methods:</p>
                <ul class="default-bullets">
                    <li>Providing an official notation of completion from the local school district.</li>
                    <li>Providing the official score report of the <a class="external-link" href="http://www.tasctest.com/" target="_blank">Test Assessing Secondary Completion (TASC™)</a>.</li>
                    <li>Providing a college transcript indicating the student has taken and successfully completed the required 24 college credits as noted in the Commissioner's Regulations.</li>
                </ul>
                <h4>Important Notes</h4>
                <ul class="default-bullets">
                    <li>Residents of New York State may not use a high school program of correspondence study to meet the requirements for high school in New York State (NYSED-3/20/2008).</li>
                    <li>A national matriculation examination from a foreign country will not be recognized as evidence of graduation.</li>
                </ul>
                <h4>Meeting Preliminary Education</h4>
                <p><strong>A Candidate for a Degree Must Meet Preliminary Education Requirements Before Being Awarded A College Degree.</strong></p> 
                <p>The requirements are different for students of compulsory school age and students beyond compulsory school age. Students beyond compulsory school age may show preliminary education through six alternatives. The alternatives for students of compulsory school age are more limited. Students of compulsory school age must be enrolled in a four-year high school program unless they have already completed such program. Therefore, they must show completion of a high school education or its substantial equivalent, as certified by the Superintendent of Schools or comparable chief school administrator, before being awarding a college degree. For more information about these requirements, please visit the <a class="external-link" href="http://www.p12.nysed.gov/nonpub/homeinstruction/homeinstruct.html" target="_blank">New York State Education Department</a>.</p>
